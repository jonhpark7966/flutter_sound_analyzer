enum PaletteType{
  jet,
  inferno,
}

class ColorPalette {
  PaletteType type;
  List<Color> palette;

  ColorPalette(this.type){
    if(type == PaletteType.jet){
      palette = jet;
    }else if(type == PaletteType.inferno){
      palette = inferno;
    }
    else{
      palette = jet;
    }
  }

  Color getColor(value, min, max){
    var pos = (value-min)/(max - min);
    if (pos>1) pos = 1;
    if (pos<0) pos = 0;
    return palette[((palette.length-1)*pos).round()];
  }

  getPaletteBitmap(palette){
    var bytes = new Uint8List(4*palette.length);
    for(var i = 0; i < palette.length; ++i){
        bytes[4*i] = palette[i].red;
        bytes[4*i+1] = palette[i].green;
        bytes[4*i+2] = palette[i].blue;
        bytes[4*i+3] = 255;
    }
    return Bitmap.fromHeadless(palette.length, 1, bytes);
  }
}


